package lab.java_project.BASIC.OBJECT클래스.toString만들기;

/**
 *
 * 1. 생성된(객체) 기준으로 메모리(힙)영역에 올라감 (생성된 객체의 메서드들이 우선순위)
 * 2. 대입받은 객체에 따라서 사용가능 (parent로 받으면 parent안의 범위 메서드들만 사용가능, Object면 object가 사용할수있는 메서드들만 사용 가능)
 * 3. 즉, 메서드 찾는 순서 : 생성된 객체(1) > 상속받은 부모객체(2) > Object 객체(3) (최후)
 * (오버라이딩을 통해 같은 메서드들의 경우, 우선순위가 높은 메서드들을 이용할수 있게 할 수 있다)
 */
public class _정리 {
    public static void main(String[] args) {
        /***
         * ✅ 자바 객체의 메모리 및 메서드 호출 원리 정리
         *
         * 객체가 생성되면, 해당 클래스의 모든 필드와 메서드는 힙(Heap) 메모리에 올라간다.
         * → 실제 생성된 객체 기준으로 메모리 구조가 구성된다.
         *
         * 참조 변수의 타입에 따라 사용할 수 있는 메서드 범위가 달라진다.
         * EX)
         * Parent parent = new Child(); → Parent에 선언된 메서드만 직접 호출 가능
         * Object obj = new Child(); → Object에 선언된 메서드만 직접 호출 가능
         *
         * 메서드 호출 우선순위 (동적 바인딩 기준):
         * - 생성된 객체의 클래스 → 상속받은 부모 클래스 → Object 클래스
         * - 같은 이름의 메서드가 여러 클래스에 존재할 경우, 가장 먼저 찾은(=가장 구체적인) 메서드를 호출함
         *
         * 이를 오버라이딩(overriding) 으로 활용할 수 있음
         */
    }
}
